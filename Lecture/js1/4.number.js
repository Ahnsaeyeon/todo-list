// number
// 정수, 실수, 2진수, 8진수, 16진수 등의 숫자를 표현 가능

let integer = 10;
console.log("integer", integer);

let double = 10.12;
console.log("double", double);

let binary = 0b1000;
console.log("binary", binary);

let octal = 0o12;
console.log("octal", octal);

let hex = 0xa;
console.log("hex", hex);

// NaN
// 숫자가 아닌 값
let nan = "문자열" / 2;
console.log("nan", nan);

// Infinity
// 무한대
let infinity = 1 / 0;
console.log("infinity", infinity);
